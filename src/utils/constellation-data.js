/**
 * Генерує SVG елемент для сузір'я
 * @param {string} signId - ID знаку зодіаку
 * @returns {string} SVG HTML string
 */
export function generateConstellationSVG(signId) {
    const data = CONSTELLATIONS[signId];
    if (!data) {
        console.warn(`[Constellation] Unknown sign: ${signId}`);
        return '';
    }

    const { stars, lines } = data;

    // Генеруємо лінії
    const linesHTML = lines.map(([start, end], index) => {
        const [x1, y1] = stars[start];
        const [x2, y2] = stars[end];
        return `<line 
            x1="${x1}" y1="${y1}" 
            x2="${x2}" y2="${y2}" 
            class="constellation-line" 
            style="animation-delay: ${index * 0.3}s"
        />`;
    }).join('\n');

    // 🔥 NEW: Лінії для ефекту бліку (бігучий вогник)
    // Використовуємо pathLength="100" щоб уніфікувати анімацію dasharray
    const shimmerLinesHTML = lines.map(([start, end], index) => {
        const [x1, y1] = stars[start];
        const [x2, y2] = stars[end];
        return `<line 
            x1="${x1}" y1="${y1}" 
            x2="${x2}" y2="${y2}" 
            class="constellation-line-shimmer" 
            pathLength="100"
            style="animation-delay: ${3.5 + index * 0.2}s"
        />`;
    }).join('\n');

    // Генеруємо зірки як ромби (diamonds)
    const starsHTML = stars.map(([x, y, size], index) => {
        // Розмір ромба залежить від size (1-3), але менший ніж раніше
        const s = size * 1.2 + 1; // Менший розмір: 2.2, 3.4, 4.6
        // Ромб: 4 точки навколо центру (x, y)
        const points = `${x},${y - s} ${x + s},${y} ${x},${y + s} ${x - s},${y}`;
        return `<polygon 
            points="${points}" 
            class="constellation-star star-size-${size}" 
            style="animation-delay: ${index * 0.15}s"
        />`;
    }).join('\n');

    return `
        <svg 
            class="constellation-svg" 
            viewBox="0 0 180 130" 
            preserveAspectRatio="xMidYMid meet"
        >
            <defs>
                <filter id="glow" x="-50%" y="-50%" width="200%" height="200%">
                    <feGaussianBlur stdDeviation="1.5" result="coloredBlur"/>
                    <feMerge>
                        <feMergeNode in="coloredBlur"/>
                        <feMergeNode in="SourceGraphic"/>
                    </feMerge>
                </filter>
            </defs>
            <g class="constellation-group" filter="url(#glow)">
                ${linesHTML}
                ${starsHTML}
            </g>
            <!-- 🔥 NEW: Шар бліку поверх основних ліній -->
            <g class="shimmer-layer" pointer-events="none">
                ${shimmerLinesHTML}
            </g>
        </svg>
    `;
}

